# Assignment operator is as simple as it sounds It assign a value to a variable 

a = 5

# a is a variable
# 5 is a value

# a = 5 meaning assign ariable a to have value of 5

# Later on when you want to use the value of a, you can just refer to a
print(a)  # Output: 5
# You can also change the value of a
a = 10
print(a)  # Output: 10
# You can also assign a value to multiple variables at once
a, b = 20, 30
print(a)  # Output: 20
print(b)  # Output: 30
# You can also assign the same value to multiple variables
a = b = c = 40
print(a)  # Output: 40
print(b)  # Output: 40
print(c)  # Output: 40
# You can also assign a value to a variable using an expression
a = 5
b = 10
c = a + b
print(c)  # Output: 15
