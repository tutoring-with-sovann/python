# Exercise: Working with Employee Performance Data

# Description:
# You are given a list named 'employees', where each element is a dictionary representing
# an employee with the following keys:
# - 'id' (integer)
# - 'name' (string)
# - 'completed_tasks' (integer)
# - 'on_time' (boolean)

# Tasks:
# 1. Print the total sum of all employee IDs.
# 2. Print the total number of completed tasks by all employees.
# 3. Print how many employees completed their tasks on time.
# 4. Print the average number of tasks completed per employee.
# 5. Print the name(s) of employees who did NOT complete tasks on time.