# Exercise 5: Reverse Words in a String
# Problem Description:
# Create a function that takes a string and reverses the order of its words while preserving the original spacing.
# A word is a sequence of non-space characters separated by one or more spaces.
# The function should:
# 1. Split the string into words, ignoring multiple spaces.
# 2. Reverse the order of the words.
# 3. Reconstruct the string with the original spacing (same number of spaces in the same positions).
# 4. If the input is empty or contains only spaces, return a string with the same number of spaces.
# Example: For "Hello World", return "World Hello".
# Example: For "Hi  There", return "There  Hi" (preserving double space).
# Example: For "  ", return "  " (preserving spaces).